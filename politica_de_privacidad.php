							<p>Conscientes de las necesidad de promover el máximo respeto de la intimidad y privacidad de los usuarios de Internet; conforme a lo dispuesto en el artículo 19 N°4 de la Constitución Política de la República de Chile; a las normas pertinentes de la Ley N° 19.628 sobre protección de la vida privada y sus modificaciones posteriores; y, a las normas internacionales que rigen la materia, el tratamiento de datos personales que se realiza en el portal <a href="http://www.atempus.cl">www.atempus.cl</a> se rige por las siguientes reglas:</p>
							<p>HEALTHCO S.A. asegura la confidencialidad de los datos personales de los SUSCRIPTORES que se registren como tales en nuestro sitio web mediante el o los formulario(s) establecido(s) al efecto o que hayan sido referidos por otros SUSCRIPTORES. HEALTHCO S.A. efectuará tratamiento interno de datos personales respecto de aquellos que han sido entregados voluntariamente por los SUSCRIPTORES en los referidos formularios. HEALTHCO S.A. también asegura la confidencialidad de los datos personales de los REFERIDOS que hayan sido facilitados por SUSCRIPTORES.</p>
							<p>La única información proporcionada por los SUSCRIPTORES relativa a sus REFERIDOS corresponde a sus correos electrónicos. Una vez individualizado el titular del correo electrónico de un REFERIDO mediante la validación de este, el REFERIDO pasa a ser un SUSCRIPTOR, pudiendo gozar de todos los derechos que este posee.</p>
							<p>Los datos personales de los SUSCRIPTORES sólo serán utilizados para el cumplimiento de los fines indicados en el formulario correspondiente. </p>
							<p>Los datos personales de los REFERIDOS, corresponden únicamente a una dirección de correo electrónico. Esta dirección de correo electrónico sólo será utilizada para la inclusión a listas de correos, pudiendo el receptor de ellas renunciar en cualquier momento utilizando el link de eliminación provisto en cualquiera de los correos electrónicos que reciba como miembro de esta lista.</p>
							<p>HEALTHCO S.A. no comunicará ni transferirá a terceros los datos personales de sus SUSCRIPTORES ni REFERIDOS sin el consentimiento expreso del titular. No obstante lo anterior, en caso de ser requerido judicialmente al efecto procederá a hacer entrega de la información solicitada.</p>
							<p>HEALTHCO S.A. podrá comunicar a terceros información estadística elaborada a partir de los datos personales de sus SUSCRIPTORES y REFERIDOS, sin el consentimiento expreso del titular, cuando de dichos datos no sea posible identificar individualmente a los titulares, de conformidad a la Ley;</p>
							<p>El titular de los datos personales podrá en todo momento ejercer los derechos otorgados por la Ley N° 19.628 y sus modificaciones posteriores. En específico, podrá:</p>
							<ul>
								<li>Solicitar información acerca de qué datos personales se mantienen registrados;</li>
								<li>Solicitar que se modifiquen sus datos personales cuando ellos no sean correctos o no estén actualizados;</li>
								<li>Solicitar la eliminación o cancelación de los datos entregados cuando así lo desee;</li>
								<li>Oponerse a que sus datos personales sean utilizados para fines estadísticos.</li>
							</ul>
							
							<p>Para ejercer sus derechos el SUSCRIPTOR o REFERIDO debe seguir las instrucciones indicadas en las <a href="preguntas_frecuentes" class="links">Preguntas Frecuentes</a>.
							<p>Respecto de la recolección y tratamiento de datos realizado mediante mecanismos automatizados con el objeto de generar registros de actividad de los VISITANTES y registros de audiencia, HEALTHCO S.A. sólo utiliza dicha información para la elaboración de informes que cumplan con los objetivos señalados. En ningún caso se realizan operaciones que permitan asociar dicha información a algún usuario identificado o identificable.
							<p>Para efectos de la presente Política de Privacidad, se entiende por:
							<ul>
								<li>SUSCRIPTOR a la persona que voluntariamente se registre en atempus.cl en los formularios especialmente establecidos al efecto o que se suscriba a la lista de correos electrónicos.</li>
								<li>REFERIDO a la persona que fue invitada por un SUSCRIPTOR a conocer los servicios ofrecidos por HEALTHCO S.A. proporcionándole a HEALTHCO S.A. el correo electrónico de esta última.</li>
								<li>VISITANTE es aquella persona que accede libremente a la información disponible en atempus.cl sin necesidad de registrarse.</li>
							</ul>
							
